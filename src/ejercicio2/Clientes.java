    public void activarCampos(boolean C1, boolean C2, boolean C3, boolean C4,boolean C5, boolean C6){
        nif.setEditable(C1);
        nombre.setEditable(C2);
        direccion.setEditable(C3);
        poblacion.setEditable(C4);
        provincia.setEditable(C5);
        telefono.setEditable(C6);
    }
