    private void BotonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAceptarActionPerformed
    try {
        String vNif, vNombre, vDireccion, vPoblacion, vProvincia, vTelefono;
        vNif = nif.getText();
        vNombre = nombre.getText();
        vDireccion = direccion.getText();
        vPoblacion = poblacion.getText();
        vProvincia = provincia.getText();
        vTelefono = telefono.getText();
        // conectar la base de datos
        String url = "jdbc:mysql://localhost:3306/bases";
        String user = "root";
        String pass = "";
        conexion = DriverManager.getConnection(url, user, pass);
        Statement s = conexion.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
      
        // Generar la instruccion de borrar
        if (esNuevo == 'B'){
        String BorraRegistro = "DELETE FROM clientes WHERE NIF='" + vNif + "'";
        int controlBorra = s.executeUpdate(BorraRegistro);
        /* r.refreshRow(); */
        }
        // Generar la instruccion de actualizar        
        if (esNuevo == 'M'){
        String ActualizaRegistro = "update clientes set NOMBRE='" + vNombre + "', DIRECCION='" + vDireccion + "', POBLACION='" + vPoblacion + "', PROVINCIA='" + vProvincia + "', TELEFONO='" + vTelefono + "' WHERE NIF='" + vNif +"'";
        int controlActualiza = s.executeUpdate(ActualizaRegistro);
        r.refreshRow();
        }
        // Generar la instruccion de Nuevo Registro
        if (esNuevo == 'N'){
        String NuevoRegistro = "insert into clientes values ('" + vNif + "','" + vNombre + "','" + vDireccion + "','" + vPoblacion + "','" + vProvincia + "','" + vTelefono + "')";
        int controlNuevo = s.executeUpdate(NuevoRegistro);
        /* r.refreshRow(); */
        }
        // Volver a refrescar la base de datos
        if (esNuevo != 'M'){
        String query = "select * from clientes";   
            r = s.executeQuery(query);
            r.first();
            sacaRegistro();
            }
    } catch (SQLException ex) {
        Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
    }
        // Volver activar los botones
        muestraBotones(true, true, true, true, true, true, true, false, false);
        activarCampos(false, false, false, false, false, false);
        LabelBorrar.setVisible(false); 
   
        // Variable control boton
            esNuevo = ' '; 
    }//GEN-LAST:event_BotonAceptarActionPerformed

